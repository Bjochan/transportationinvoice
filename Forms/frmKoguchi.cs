        void Calc()
        {
            gridView3.PostEditor();
            string Transit = gridView3.GetFocusedRowCellValue("経由").ToString();
            string Departure = gridView3.GetFocusedRowCellValue("到着地").ToString();
            string Arrival = gridView3.GetFocusedRowCellValue("出発地").ToString();
            int mround =1;
            if (gridView3.GetFocusedRowCellValue("乗方").ToString() == "往復")
                mround=2;
            if (Transit != "")
            {
                string sql = "(Arrival='" + Arrival + "' AND Departure='" + Transit + "') or (Arrival='" + Transit + "' AND Departure='" + Arrival + "')";
                DataRow[] dr = _mkeiro.Select(sql, "");
                if (dr != null && dr.Length > 0)
                {
                    gridView3.SetFocusedRowCellValue("乗物1", dr[0]["Transportation"]);
                    gridView3.SetFocusedRowCellValue("値段1", dr[0]["Fare"]);
                  
                }
                else
                {
                    MessageBox.Show("経路が見つけません");
                    gridView3.SetFocusedRowCellValue("乗物1", null);
                    gridView3.SetFocusedRowCellValue("値段1", null);
                }
            }
            else
            {
                gridView3.SetFocusedRowCellValue("乗物1", null);
                gridView3.SetFocusedRowCellValue("値段1", null);
            }

            if (Departure != "" && Transit=="")
            {
                string sql = "(Arrival='" + Arrival + "' AND Departure='" + Departure + "') or (Arrival='" + Departure + "' AND Departure='" + Arrival + "')";
                DataRow[] dr = _mkeiro.Select(sql, "");
                if (dr != null && dr.Length > 0)
                {
                    gridView3.SetFocusedRowCellValue("乗物2", dr[0]["Transportation"]);
                    gridView3.SetFocusedRowCellValue("値段2", dr[0]["Fare"]);
                   
                }
                else
                {
                  
                    gridView3.SetFocusedRowCellValue("乗物2", null);
                    gridView3.SetFocusedRowCellValue("値段2", null);
                }
            }
            else if (Departure != "" && Transit != "")
            {
                string sql = "Arrival='" + Transit + "' AND Departure='" + Departure + "'";
                DataRow[] dr = _mkeiro.Select(sql, "");
                if (dr != null && dr.Length > 0)
                {
                    gridView3.SetFocusedRowCellValue("乗物2", dr[0]["Transportation"]);
                    gridView3.SetFocusedRowCellValue("値段2", dr[0]["Fare"]);

                }
                else
                {
                    MessageBox.Show("経路が見つけません");
                    gridView3.SetFocusedRowCellValue("乗物2", null);
                    gridView3.SetFocusedRowCellValue("値段2", null);
                }
            }
            else
            {
                
                gridView3.SetFocusedRowCellValue("乗物2", null);
                gridView3.SetFocusedRowCellValue("値段2", null);
            }


            decimal faretodepart = 0;
            try
            {
                faretodepart = decimal.Parse(gridView3.GetFocusedRowCellValue("値段2").ToString());
            }
            catch { }
            decimal faretotransit = 0;
            try
            {
                faretotransit = decimal.Parse(gridView3.GetFocusedRowCellValue("値段1").ToString());
            }
            catch { }
            
            gridView3.SetFocusedRowCellValue("金額", (faretotransit + faretodepart)*mround);
        }
